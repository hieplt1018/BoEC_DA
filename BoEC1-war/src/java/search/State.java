package search;

/**
 *
 * @author asus
 */
public class State<A, B>{
    public A a;
    public B b;

    public State() {
    }

    
    public State(A a, B b) {
        this.a = a;
        this.b = b;
    }

    public A getA() {
        return a;
    }

    public void setA(A a) {
        this.a = a;
    }

    public B getB() {
        return b;
    }

    public void setB(B b) {
        this.b = b;
    }
    
    
}
